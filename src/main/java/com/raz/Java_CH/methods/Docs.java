package com.raz.Java_CH.methods;

public class Docs {
    public static void main(String[] args) {

       // A method in Java is a block of code designed to perform a specific task. Methods are used to organize code, reduce duplication, and enhance reusability.

      /*  access_modifier return_type method_name(parameter_list) {
            // Method body (code to execute)
            return value; // Optional (if return type is not void)
        }*/

       /*

       1. Access Modifier:

        Defines the visibility of the method (e.g., public, private, protected, or default).

        Example: public means the method can be accessed from anywhere.

        2. Return Type:

        Specifies the type of value the method returns.

        Use void if the method does not return any value.

        Example: int, String, double, etc.

        3. Method Name:

        The name of the method, which should follow Java naming conventions (camelCase).

        Example: calculateSum, printMessage.

        4. Parameter List:

        A comma-separated list of input parameters (arguments) that the method accepts.

        Example: (int a, int b).

        5. Method Body:

        The block of code that defines what the method does.

        6. Return Statement:

        Used to return a value to the caller (if the return type is not void).

        Example: return a + b;.*/
    }
}
